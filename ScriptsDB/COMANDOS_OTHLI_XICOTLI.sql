-- Verificacion de creacion de indices
SELECT 
    TABLE_NAME AS tabla,
    INDEX_NAME AS nombre_indice,
    GROUP_CONCAT(COLUMN_NAME ORDER BY SEQ_IN_INDEX) AS columnas,
    CASE NON_UNIQUE 
        WHEN 0 THEN 'UNIQUE' 
        ELSE 'NORMAL' 
    END AS tipo,
    INDEX_COMMENT AS comentario
FROM information_schema.STATISTICS
WHERE TABLE_SCHEMA = DATABASE()
  AND INDEX_NAME != 'PRIMARY'  -- Oculta la PK para no saturar
GROUP BY TABLE_NAME, INDEX_NAME, NON_UNIQUE, INDEX_COMMENT
ORDER BY TABLE_NAME, INDEX_NAME;

